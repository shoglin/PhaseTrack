function plotQuiver(x1, x2, dx)
% Функция построения стрелок, указывающих направление фазовых траекторий
%
% Входные параметры:
%   x1, x2 - матрицы сетки (от meshgrid)
%   dx - функция градиента @(t, x)

hold on;

% Вычисляем градиент во всех точках сетки
% Преобразуем матрицы в векторы: [x1(:)'; x2(:)']
z = dx(0, [reshape(x1, 1, []); reshape(x2, 1, [])]);

% Извлекаем компоненты производных
dx1 = z(1, :);
dx2 = z(2, :);

% Возвращаем обратно к форме матриц исходного размера
dx1 = reshape(dx1, size(x1, 1), size(x1, 2));
dx2 = reshape(dx2, size(x2, 1), size(x2, 2));

% Нормализуем стрелки для равномерной длины (лучшая визуализация)
magnitude = sqrt(dx1.^2 + dx2.^2);
dx1_norm = dx1 ./ magnitude;
dx2_norm = dx2 ./ magnitude;

% Рисуем векторное поле
quiver(x1, x2, dx1_norm, dx2_norm, ...
       'k', ...                        % черный цвет
       'AutoScale', 'on', ...          % автоматическое масштабирование
       'AutoScaleFactor', 0.35, ...    % размер стрелок
       'LineWidth', 0.8, ...           % толщина линий
       'MaxHeadSize', 0.5);            % размер головки стрелки

hold off;
end