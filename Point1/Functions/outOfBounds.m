function [value, isterminal, direction] = outOfBounds(t, z, bound)
% Функция-событие для остановки интегрирования при выходе за границы
%
% Входные параметры:
%   t - текущее время
%   z - вектор состояния [x1; x2]
%   bound - граничное значение
% Выходные параметры:
%   value - значение функции события (равно нулю при наступлении события)
%   isterminal - флаг остановки интегрирования (1 - остановить)
%   direction - направление пересечения нуля (0 - любое)

value = [abs(z(1)) - bound; abs(z(2)) - bound]; % событие: достижение границы
isterminal = [1; 1];                            % остановить при любом событии
direction = [0; 0];                             % любое направление пересечения
end