%% main.m - Главный исполнительный файл
clc;
clear all;
close all;

tic; % запуск секундомера

%% Параметры моделирования
XMAX = 5;      % размер сетки рисования (от -5 до 5 по обеим осям)
STEP = 0.5;    % шаг сетки для векторного поля
TMAX = 10;     % время моделирования

%% Создание сетки для векторного поля
[x1, x2] = meshgrid(-XMAX: STEP: XMAX);

%% Определение системы
% Уравнение: d2x/dt2 - (dx/dt)*x - 2*x^3 + 5*x - 1 = 0
% В нормальной форме:
%   x1' = x2
%   x2' = x1*x2 + 2*x1^3 - 5*x1 + 1
dx = @(t, x) mySystem(t, x);

% Функция-событие для остановки интегрирования при выходе за границы
event_out_of_bounds = @(t, z) outOfBounds(t, z, XMAX + 0.01);

%% Поиск и отображение особых точек
fprintf('Поиск особых точек...\n');
equilibria = findEquilibria(XMAX);
fprintf('\n');

%% Построение фазового портрета
fprintf('Построение фазового портрета...\n');
plotLocus(x1, x2, dx, event_out_of_bounds, TMAX, equilibria);

%% Построение векторного поля
fprintf('Построение векторного поля...\n');
plotQuiver(x1, x2, dx);

toc; % остановка секундомера


