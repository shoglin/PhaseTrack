function [value, isterminal, direction] = outOfBounds3D(t, z, xmax, ymax, zmax)
% OUTOFBOUNDS3D Функция-событие для остановки интегрирования при выходе за границы
%   [value, isterminal, direction] = OUTOFBOUNDS3D(t, z, xmax, ymax, zmax)
%   возвращает условия остановки интегрирования, когда траектория выходит
%   за пределы области [-xmax, xmax] по x, [-ymax, ymax] по y, [-zmax, zmax] по z
%
%   Входные параметры:
%       t     - текущее время
%       z     - текущее состояние [x; y; z]
%       xmax  - максимальное значение x
%       ymax  - максимальное значение y
%       zmax  - максимальное значение z
%
%   Выходные параметры:
%       value       - значение функции события (0 при пересечении границы)
%       isterminal  - 1, если нужно остановить интегрирование
%       direction   - 0 для всех пересечений

x = z(1);
y = z(2);
z_val = z(3);

% Проверяем выход за границы по каждой координате
value = [abs(x) - xmax; abs(y) - ymax; abs(z_val) - zmax];
isterminal = [1; 1; 1];  % Останавливаем при любом пересечении
direction = [0; 0; 0];   % Останавливаем при любом направлении

end